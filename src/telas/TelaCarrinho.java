public TelaCarrinho() {
        initComponents();
           readJtable();
        tabelaCarrinho.getSelectionModel().addListSelectionListener(new ListSelectionListener(){
           public void valueChanged(ListSelectionEvent event){
               if(!event.getValueIsAdjusting()){
                   int linhaSelecionada = tabelaCarrinho.getSelectedRow();
                   if(linhaSelecionada != -1){
                   int idAtual=(int)tabelaCarrinho.getValueAt(linhaSelecionada, 0);
                   carrinho.setId_produtos_pedido(idAtual);
               }
           }    
           } 
        });
    }

    
public void readJtable(){
    DefaultTableModel modelo = (DefaultTableModel) tabelaCarrinho.getModel();
    modelo.setNumRows(0);    
   for(CarrinhoDTO p: carrinhos.read()){
       modelo.addRow(new Object[]{
        p.getId_produtos_pedido(),
        p.getQuantidade(),
        p.getPreco_unitario()
        });
   }
}
